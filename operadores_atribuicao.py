# Atribuição Simples
Saldo = 300

# Atribuição com adição

Saldo += 100
print(Saldo)
# Atribuição com subtração

Saldo -= 100
print(Saldo)

# Atribuição com multiplicação

Saldo *= 2
print(Saldo)

# Atribuição com divisão Normal

Saldo /= 5 
print(Saldo)

# Atribuição com divisão inteira

Saldo //= 5
print(Saldo)

# Atribuição com módulo

Saldo %= 480
print(Saldo)

# Atribuição com exponenciação

Saldo **= 2
print(Saldo)




